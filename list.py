# Python program showing 5 list methods

# 1. append()
numbers = [1, 2, 3]
numbers.append(4)
print("After append:", numbers)

# 2. insert()
fruits = ["apple", "banana"]
fruits.insert(1, "mango")
print("After insert:", fruits)

# 3. remove()
colors = ["red", "blue", "green", "blue"]
colors.remove("blue")
print("After remove:", colors)

# 4. pop()
letters = ["a", "b", "c", "d"]
letters.pop(2)
print("After pop:", letters)

# 5. sort()
nums = [5, 2, 8, 1]
nums.sort()
print("After sort:", nums)
